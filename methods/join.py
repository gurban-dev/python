'''
Built-in join() method that belongs to
an object of the Python string class.

The str.join() method in Python is used to
concatenate the elements of an iterable
(such as a list, tuple, set, or dictionary)
into a single string, with each element
separated by the string on which join() is
called (the separator).

Syntax:
separator.join(iterable)
'''
" ".join(["Good", "day!"])