# In Python, an f-string (short for Formatted string
# literal) lets you put variables directly inside a
# string literal using {} brackets. This makes your
# code shorter and easier to read.

name = "Herodotus"
century = 5

# Using an f-string
print(f"The father of history is {name}. He lived during the {century}th century BC.")

